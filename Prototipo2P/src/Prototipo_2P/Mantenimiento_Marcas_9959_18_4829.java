    private void jButton_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_BuscarActionPerformed
        // TODO add your handling code here:
        
            try
            {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/sic", "root", "Langas798");
            PreparedStatement pst = cn.prepareStatement("select * from Marcas where Codigo_Marca = ?");
            pst.setString(1, txt_Buscar.getText().trim());
            
            ResultSet rs = pst.executeQuery();
            
            if(rs.next())
            {
                txt_Codigo_Marcas.setText(rs.getString("Codigo_Marca"));
                txt_Nombre_Marca.setText(rs.getString("Nombre_Marca"));
                txt_Estatus_Marca.setText(rs.getString("Estatus_Marca"));
            } else 
            {
                JOptionPane.showMessageDialog(null, "Marca no registrado.");
            }
            
        }catch (Exception e){
                System.out.println(e);
        }
    }//GEN-LAST:event_jButton_BuscarActionPerformed


